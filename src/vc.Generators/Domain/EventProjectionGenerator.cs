        sb.AppendLine(value: "    /// </summary>");
        sb.AppendLine(value: "    public void Handle(object @event, long version)");
        sb.AppendLine(value: "    {");
        sb.AppendLine(value: "        if (@event == null)");
        sb.AppendLine(value: "            throw new ArgumentNullException(nameof(@event), \"Event cannot be null.\");");
        sb.AppendLine(value: "        ApplyEvent(@event);");
        sb.AppendLine(value: "        Version = version;");
        sb.AppendLine(value: "        LastUpdated = DateTime.UtcNow;");
        sb.AppendLine(value: "    }");
        sb.AppendLine(value: "}");
        sb.AppendLine();
        sb.AppendLine(value: "/// <summary>");
        sb.AppendLine(value: $"/// Event projection (read model) for {model.ProjectionName}.");
        sb.AppendLine(value: "/// </summary>");
        sb.AppendLine(value: $"public sealed partial class {model.ProjectionName}Projection : EventProjection");
        sb.AppendLine(value: "{");
        sb.AppendLine(value: $"    /// <summary>Gets the aggregate root ID associated with this projection.</summary>");
        sb.AppendLine(value: $"    public Guid AggregateId {{ get; set; }}");
        sb.AppendLine();
        sb.AppendLine(value: "    /// <summary>");
        sb.AppendLine(value: "    /// Handles an event and updates the projection state.");
        sb.AppendLine(value: "    /// </summary>");
        sb.AppendLine(value: "    protected override void ApplyEvent(object @event)");
        sb.AppendLine(value: "    {");
        sb.AppendLine(value: "        if (@event == null)");
        sb.AppendLine(value: "            throw new ArgumentNullException(nameof(@event), \"Event cannot be null.\");");
        sb.AppendLine(value: "        // Event handler dispatch implemented by derived classes");
        sb.AppendLine(value: "    }");
        sb.AppendLine(value: "}");

        ctx.AddSource(hintName: $"{model.ProjectionName}Projection.g.cs", sourceText: SourceText.From(text: sb.ToString(), encoding: Encoding.UTF8));
    }

    private sealed record EventProjectionModel(string ProjectionName, string Namespace);
}
