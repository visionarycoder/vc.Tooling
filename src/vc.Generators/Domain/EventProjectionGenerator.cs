        sb.AppendLine(value: "// <auto-generated by VisionaryCoder.Tooling.Generators />");
        sb.AppendLine(value: "#nullable enable");
        sb.AppendLine();
        sb.AppendLine(value: $"namespace {model.Namespace};");
        sb.AppendLine();
        sb.AppendLine(value: "/// <summary>");
        sb.AppendLine(value: "/// Base class for event projections (read models).");
        sb.AppendLine(value: "/// </summary>");
        sb.AppendLine(value: "public abstract partial class EventProjection");
        sb.AppendLine(value: "{");
        sb.AppendLine(value: "    /// <summary>Gets the projection ID.</summary>");
        sb.AppendLine(value: "    public Guid Id { get; protected set; } = Guid.NewGuid();");
        sb.AppendLine();
        sb.AppendLine(value: "    /// <summary>Gets the last event version applied.</summary>");
        sb.AppendLine(value: "    public long Version { get; protected set; }");
        sb.AppendLine();
        sb.AppendLine(value: "    /// <summary>Gets the last update timestamp.</summary>");
        sb.AppendLine(value: "    public DateTime LastUpdated { get; protected set; } = DateTime.UtcNow;");
        sb.AppendLine();
        sb.AppendLine(value: "    /// <summary>");
        sb.AppendLine(value: "    /// Handles an event and updates the projection state.");
        sb.AppendLine(value: "    /// </summary>");
        sb.AppendLine(value: "    protected abstract void ApplyEvent(object @event);");
        sb.AppendLine();
        sb.AppendLine(value: "    /// <summary>");
