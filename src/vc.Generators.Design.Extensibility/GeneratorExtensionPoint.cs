                var symbol = model.GetDeclaredSymbol(method) as IMethodSymbol;
                if (symbol is null) continue;

                var className = symbol.ContainingType.Name;
                var methodName = symbol.Name;
                var returnType = symbol.ReturnType.ToDisplayString();

                var attr = symbol.GetAttributes()
                    .First(a => a.AttributeClass!.Name.Contains("GenerateRetry"));
                var attempts = (int)attr.ConstructorArguments[0].Value!;

                sb.AppendLine($$"""
                    public static class {{className}}_{{methodName}}_Retry
                    {
                        public static {{returnType}} Invoke({{className}} instance)
                        {
                            int attempts = {{attempts}};
                            while (true)
                            {
                                try { return instance.{{methodName}}(); }
                                catch when (--attempts > 0) { }
                            }
                        }
                    }
                """);
            }

            sb.AppendLine("}");

            context.AddSource("GeneratedRetryPolicies.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
