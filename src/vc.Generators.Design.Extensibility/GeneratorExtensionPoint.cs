            public void OnVisitSyntaxNode(SyntaxNode node)
            {
                if (node is EnumDeclarationSyntax e &&
                    e.AttributeLists.Any(a => a.Attributes.Any(attr =>
                        attr.Name.ToString().Contains("GenerateErrorCodes"))))
                {
                    Candidates.Add(e);
                }
            }
        }
    }

