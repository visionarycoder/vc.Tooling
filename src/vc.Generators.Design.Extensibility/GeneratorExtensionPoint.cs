using System;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;
using System.Linq;
using System.Text;

namespace VisionaryCoder.Generators.Design.Extensibility;

    [Generator]
    public sealed class CachingGenerator : ISourceGenerator
    {
    public void Initialize(GeneratorInitializationContext context)
    {
        context.RegisterForSyntaxNotifications(() => new Receiver());
    }

    public void Execute(GeneratorExecutionContext context)
        {
            if (context.SyntaxReceiver is not Receiver receiver)
                return;

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated />");
            sb.AppendLine("namespace VisionaryCoder.Caching.Generated {");

            foreach (var method in receiver.Candidates)
            {
                var model = context.Compilation.GetSemanticModel(method.SyntaxTree);
                var symbol = model.GetDeclaredSymbol(method) as IMethodSymbol;
                if (symbol is null) continue;

                var className = symbol.ContainingType.Name;
                var methodName = symbol.Name;
                var returnType = symbol.ReturnType.ToDisplayString();

                sb.AppendLine($$"""
                    public static class {{className}}_{{methodName}}_Cache
                    {
                        private static {{returnType}}? _cached;
                        private static bool _hasValue;

                        public static {{returnType}} Invoke({{className}} instance)
                        {
                            if (_hasValue) return _cached!;
                            _cached = instance.{{methodName}}();
                            _hasValue = true;
                            return _cached!;
                        }
                    }
                """);
            }

            sb.AppendLine("}");

            context.AddSource("GeneratedCaching.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
        }

        private sealed class Receiver : ISyntaxReceiver
        {
            public List<MethodDeclarationSyntax> Candidates { get; } = new();

            public void OnVisitSyntaxNode(SyntaxNode node)
            {
                if (node is MethodDeclarationSyntax m &&
                    m.AttributeLists.Any(a => a.Attributes.Any(attr =>
                        attr.Name.ToString().Contains("GenerateCache"))))
                {
                    Candidates.Add(m);
                }
            }
        }
    }

