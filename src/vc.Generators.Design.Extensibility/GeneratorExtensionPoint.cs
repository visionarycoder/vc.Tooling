    {
    public void Initialize(GeneratorInitializationContext context)
    {
        context.RegisterForSyntaxNotifications(() => new Receiver());
    }

    public void Execute(GeneratorExecutionContext context)
        {
            if (context.SyntaxReceiver is not Receiver receiver)
                return;

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated />");
            sb.AppendLine("namespace VisionaryCoder.Errors.Generated {");

            foreach (var enumDecl in receiver.Candidates)
            {
                var model = context.Compilation.GetSemanticModel(enumDecl.SyntaxTree);
                var symbol = model.GetDeclaredSymbol(enumDecl) as INamedTypeSymbol;
                if (symbol is null) continue;

                var enumName = symbol.Name;

                sb.AppendLine($"public static class {enumName}_Codes {{");

                foreach (var member in symbol.GetMembers().OfType<IFieldSymbol>())
                {
                    if (member.ConstantValue is null) continue;
                    sb.AppendLine($"    public const int {member.Name} = {member.ConstantValue};");
                }

                sb.AppendLine("}");
            }

            sb.AppendLine("}");

            context.AddSource("GeneratedErrorCodes.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
        }

